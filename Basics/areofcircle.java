import java.util.*;
public class areofcircle {
    public static void main(String[] args) {
        float pi=3.14f;
        Scanner area=new Scanner(System.in);
        System.out.println("Please enter the radius");
        int r=area.nextInt();
        System.out.println("Area of circle:="+(pi*r*r));
    }
    
}
