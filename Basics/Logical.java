import java.util.*;
public class Logical {
    //realtional operator
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println((10 > 5) && (43>2));
        System.out.println((10 > 5) && (43<2));
        System.out.println((10 < 5) && (43>2));
        System.out.println((10 < 5) && (43<2));
        System.out.println((10 > 5) || (43>2));     
        System.out.println((10 > 5) || (43<2));
        System.out.println((10 < 5) || (43>2));
        System.out.println((10 < 5) || (43<2));
        


    }
}
