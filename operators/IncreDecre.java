

public class IncreDecre {
    public static void main(String[] args) {
        int a=10;

        System.out.println("pre/post increment");
        System.out.println(a++);
        System.out.println(++a);
        int b=4;
        System.out.println(b--);
        System.out.println(--b);
    }
    
}
